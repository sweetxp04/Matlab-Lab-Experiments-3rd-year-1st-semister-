clear all;
close all;
clc;
amp =2;
f1 = input("Enter frequency of the carrier signal: ");
f2 = input("Enter frequency of the message signal: ");
t = 0.001:0.001:1;
Ct=amp.*sin(2*pi*f1*t);
Mt=square(2*pi*f2*t);
Rt =Mt.*Ct;
subplot(3,1,1);
plot(t,Mt);
title("Message Signal");
xlabel("Time in sec");
ylabel("Amplitude");
subplot(3,1,2);
plot(t,Ct);
title("Carrier Signal");
xlabel("Time in sec");
ylabel("Amplitude");
subplot(3,1,3);
plot(t,Rt);
title("PSK Signal");
xlabel("Time in sec");
ylabel("Amplitude");